    <h1>Peran Mahasiswa dalam Menjaga Pancasila</h1>
    <p>Mahasiswa memiliki peran strategis sebagai generasi penerus bangsa dalam menjaga dan mengamalkan nilai-nilai Pancasila sebagai ideologi negara. Berikut adalah beberapa peran penting mahasiswa:</p>

    <h2>Agen Perubahan Sosial</h2>
    <p>Mahasiswa sebagai agen perubahan memiliki tanggung jawab untuk membawa transformasi positif dalam masyarakat, khususnya dalam pengamalan nilai Pancasila, seperti:</p>
    <ul>
        <li>Mendorong terciptanya masyarakat yang adil dan beradab.</li>
        <li>Mengawal pelaksanaan keadilan sosial di berbagai bidang.</li>
        <li>Melawan segala bentuk diskriminasi dan intoleransi.</li>
    </ul>

    <h2>Pelopor Toleransi dan Persatuan</h2>
    <p>Dalam keragaman budaya dan agama, mahasiswa harus menjadi contoh sikap toleransi dan menjaga persatuan bangsa:</p>
    <ul>
        <li>Menjalin hubungan baik antar sesama mahasiswa dari berbagai latar belakang.</li>
        <li>Mengadakan kegiatan kebangsaan yang mengedepankan persatuan.</li>
        <li>Mencegah konflik dengan dialog dan musyawarah.</li>
    </ul>

    <h2>Penyampai Suara Kebenaran Melalui Karya</h2>
    <p>Mahasiswa juga berperan sebagai penyampai aspirasi dan pengawal kebenaran melalui berbagai karya, seperti:</p>
    <ul>
        <li>Membuat karya tulis ilmiah tentang pentingnya Pancasila.</li>
        <li>Menghasilkan konten digital yang mengedukasi masyarakat tentang nilai-nilai kebangsaan.</li>
        <li>Berpartisipasi aktif dalam kampanye sosial dan seminar kebangsaan.</li>
    </ul>

    <h2>Contoh Kontribusi Nyata Mahasiswa</h2>
    <ul>
        <li>Mengadakan aksi sosial berbasis nilai Pancasila.</li>
        <li>Mengorganisir diskusi dan seminar kebangsaan di kampus.</li>
        <li>Mengembangkan program edukasi karakter di lingkungan sekitar.</li>
    </ul>

